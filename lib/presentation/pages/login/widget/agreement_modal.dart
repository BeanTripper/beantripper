3. 회원이 다음 각 호의 어느 하나의 사유에 해당하는 경우, 회사는 회원 자격을 제한, 정지 또는 상실시킬 수 있습니다. 회원 자격이 제한된 경우 그 기간을 정하여 회사가 제공하는 서비스(리뷰, 체크인 등)의 이용이 중지될 수 있으며, 제재 기간이 만료되기 전 또는 회원자격이 정지, 상실된 경우 재가입이 제한됩니다.
   - 본 약관 제9조 및 제10조의 1에 해당하는 회원의 의무를 위반한 경우
   - 회사로부터 서비스 이용 중지, 회원 자격 정지 조치 등을 받은 회원이 그 조치기간 중에 이용계약을 임의해지하고 재회원가입 등을 통해 서비스를 이용하는 경우
